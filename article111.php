<p><i>- "У меня было трое мужей. Инициатором разводов была я сама: все они пили, распускали руки и морально издевались надо мной. Мама раньше часто говорила мне, что ничего хорошего меня в жизни не ждет, что из-за меня все беды..."</i></p>

