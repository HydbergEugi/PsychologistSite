<p>2. В ваших силах дать самому себе столько любви, сколько вы сами пожелаете. Сознательно. Сейчас. В любом возрасте. <b><u>Трансформируйте отношение к себе:</b></u></p>

<p><i>- Я много работаю, я многого достигла. Но я работаю, потому что МНЕ это нужно или МНЕ это нравится. Хочу – работаю. Хочу – отдыхаю.</i></p>

<p><i>- В моей жизни есть то, что возникло благодаря моей силе и упорству, несмотря на то, что говорила мама. Я знаю, что могу добиться всего того, что МНЕ нужно.</i></p>

