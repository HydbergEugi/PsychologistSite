<p style = "text-align:center; margin-top:40px;"><b>Что делать, если вам много лет, но вы до сих пор живете с оглядкой на родителей?</b></p>

<p>1. Первое, что нужно сделать – <b><u>признаться самому себе</u></b> в том, что всю свою жизнь вы жили, 
