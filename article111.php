<p><i>- Я буду работать как лошадь, но докажу всем, что я достойна любви и уважения!</i></p>

<p><i>- Я ни к чему не способна и нет смысла ждать любви от других.</i></p>

<p><i>- У меня не может быть ничего хорошего в жизни – я источник всех бед!</i></p>

<p><u>Как только вы поймете свой сценарий и зависимость от него – вы обезоружите его.</u> Он перестанет иметь над вами власть, и вы можете просто и легко от него избавиться.</p>

<p>Мы не в силах вложить свои понятия в другого человека – он живет «по своим понятиям». Да, не каждому удается пережить в детстве родительскую любовь, 
